    private void preencherTabelaStatus() {

        ProdutosDAO produtosDao = new ProdutosDAO();

        String status = "Vendido";

        List<ProdutosDTO> listaprodutostatus = produtosDao.getProdutoStatus(status);

        DefaultTableModel tabelaProdutosStatus = (DefaultTableModel) tblProdutosVendidos.getModel();

        tabelaProdutosStatus.setNumRows(0);

        tblProdutosVendidos.setRowSorter(new TableRowSorter(tabelaProdutosStatus));

        for (ProdutosDTO p : listaprodutostatus) { //em cada volta do la√ßo for, o mesmo adiciona uma dado(empresa) dentro do objeto c
            Object[] obj = new Object[]{
                p.getId(),
                p.getNome(),
                p.getValor(),
                p.getStatus(),};
            tabelaProdutosStatus.addRow(obj);
        }
    }

